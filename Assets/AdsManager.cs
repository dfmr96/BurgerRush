    private async void InitializeAds()
    {
        await NoAdsService.InitializeAsync();

        if (NoAdsService.HasNoAds)
        {
            Debug.Log("ðŸš« Ads disabled by NoAds purchase.");
            return;
        }

        if (IsInitialized) return;

        Debug.Log("ðŸŽ¯ UGS and NoAds ready. Initializing ads...");

        LevelPlayBootstrapper.Initialize(
            appKey,
            OnInitSuccess,
            OnInitFailed
        );
    }

    private void OnInitSuccess(LevelPlayConfiguration config)
    {
        Debug.Log("âœ… LevelPlay initialized");

        banner = new BannerAdSample();
        interstitial = new InterstitialAdSample();
        rewarded = new RewardedAdSample();

        IsInitialized = true;
        OnInitialized?.Invoke();
    }

    private void OnInitFailed(LevelPlayInitError error)
    {
        Debug.LogError("âŒ LevelPlay init failed: " + error);
    }
    
