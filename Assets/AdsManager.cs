    public void ShowBanner()
    {
#if UNITY_WEBGL
        Debug.Log("ðŸš« Banner ads not supported in WebGL.");
        return;
#endif
        if (ShouldBlockAds("banner")) return;
        if (banner == null)
        {
            Debug.LogWarning("âš ï¸ Banner ad not initialized.");
            return;
        }
        if (bannerVisible)
        {
            Debug.Log("â„¹ï¸ Banner already visible.");
            return;
        }

        banner.Show();

        if (banner.IsReady())
        {
            bannerVisible = true;
            Debug.Log("âœ… Banner is now visible.");
        }
        else
        {
            Debug.Log("â³ Banner not ready.");
        }
    }

    public void HideBanner()
    {
        if (banner == null || !bannerVisible)
        {
            Debug.Log("â„¹ï¸ Banner already hidden or not initialized.");
            return;
        }

        banner.Hide();
        bannerVisible = false;
    }

    public bool IsBannerReady() => banner != null && banner.IsReady();
    public bool IsBannerVisible() => bannerVisible;

